package pack1;

public class ROB {
	public String instrStatus;
	public String PC;
	public String destAddress;
	public String destValue;
	public String excode;
	public String FUType;
	
	public String getInstrStatus() {
		return instrStatus;
	}
	public void setInstrStatus(String instrStatus) {
		this.instrStatus = instrStatus;
	}
	public String getPC() {
		return PC;
	}
	public void setPC(String pC) {
		PC = pC;
	}
	public String getDestAddress() {
		return destAddress;
	}
	public void setDestAddress(String destAddress) {
		this.destAddress = destAddress;
	}
	public String getDestValue() {
		return destValue;
	}
	public void setDestValue(String destValue) {
		this.destValue = destValue;
	}
	public String getExcode() {
		return excode;
	}
	public void setExcode(String excode) {
		this.excode = excode;
	}
	public String getFUType() {
		return FUType;
	}
	public void setFUType(String fUType) {
		FUType = fUType;
	}
	
}
